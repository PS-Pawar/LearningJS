//data types in JavaScript

let name = "Pavan"; // string data type to store name
const age = 21; // number data type to store age, using const as age is not expected to change
let isStudent = true; // boolean data type to store student status  
let city = "Aurangabad"; // string data type to store city
let course = "JavaScript"; // string data type to store course

console.log(typeof name); // Output: string
console.log(typeof age); // Output: number
console.log(typeof isStudent); // Output: boolean
console.log(typeof city); // Output: string
console.log(typeof course); // Output: stringnode


// In JavaScript, there are several data types including:
// 1. String: Used to represent text. It is enclosed in quotes (single, double, or backticks).

// 2. Number: Used to represent numeric values, both integers and floating-point numbers.

// 3. Boolean: Used to represent true or false values.

// 4. Undefined: Used to represent a variable that has been declared but not assigned a value.

// 5. Null: Used to represent the intentional absence of any object value.

// 6. Symbol: Used to create unique identifiers for object properties.

// 7. BigInt: Used to represent integers with arbitrary precision.



